#!/usr/bin/env sh

cd $HOME;

function doIt() {
    cp .zshrc $HOME/.zshrc
    cp .profile $HOME/.profile
}

if [ "$1" == "--force" -o "$1" == "-f" ]; then
	doIt;
else
	read -p "This may overwrite existing files in your home directory. Are you sure? (y/n) " -n 1;
	echo "";
	if [[ $REPLY =~ ^[Yy]$ ]]; then
		doIt;
	fi;
fi;
unset doIt;